    private void jtxtCantidadKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxtCantidadKeyTyped
       char validar = evt.getKeyChar();
         
        if (Character.isLetter(validar)) {
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(null, "Ingrese solo n√∫meros", "Advertencia", JOptionPane.WARNING_MESSAGE);
         }

    }//GEN-LAST:event_jtxtCantidadKeyTyped
private void limpiarCampos() {
        this.jtxtCantidad.setText("");
        this.jtxtTipo.setText("");
        this.jtxtVehiculo.setText("");
    }

    private String getJsonRM() {
        JsonObject rm = new JsonObject();
        rm.addProperty("tipo", this.jtxtTipo.getText());
        rm.addProperty("cantidad", this.jtxtCantidad.getText());
        rm.addProperty("vehiculo", this.jtxtVehiculo.getText());
        return rm.toString();
    }

